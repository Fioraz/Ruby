# Push new value to the end of the array
[1, 2, 3] << 4
# ==> [1, 2, 3, 4]

# Push new string to the end of the first string
"Yukihiro " << "Matsumoto"
# ==> "Yukihiro Matsumoto"



alphabet = ["a", "b", "c"]
alphabet << "d"
puts alphabet

caption = "A giraffe surrounded by "
caption << "weezards!"
puts caption